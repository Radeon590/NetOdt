namespace NetOdt.Enumerations
{
    /// <summary>
    /// The style type for a passage
    /// </summary>
    internal enum StyleFamily
    {
        /// <summary>
        /// Style for a text paragraph
        /// </summary>
        Paragraph,

        /// <summary>
        /// Style for a table
        /// </summary>
        Table,

        /// <summary>
        /// Style for a table column
        /// </summary>
        TableColumn,

        /// <summary>
        /// Style for a table cell
        /// </summary>
        TableCell,

        /// <summary>
        /// Style for a graphic (e.g. images)
        /// </summary>
        Graphic
    }
}
