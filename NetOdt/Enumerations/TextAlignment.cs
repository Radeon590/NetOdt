namespace NetOdt.Enumerations
{
    /// <summary>
    /// The alignment of a text
    /// </summary>
    public enum TextAlignment
    {
        /// <summary>
        /// The text alignment is left
        /// </summary>
        Left,

        /// <summary>
        /// The text alignment is centered
        /// </summary>
        Center,

        /// <summary>
        /// The text alignment is right
        /// </summary>
        Right,

        /// <summary>
        /// The text use full justification
        /// </summary>
        Justify,
    }
}
